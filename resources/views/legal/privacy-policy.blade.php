    <h1 style="color:#0d2460;">Kebijakan Privasi</h1>
    <p class="updated">Terakhir diperbarui: 6 Juli 2026</p>

    <p>
        Kebijakan Privasi ini menjelaskan bagaimana aplikasi <strong>DOSMAN</strong> (tersedia dalam bentuk
        aplikasi mobile dan situs web) milik <strong>SMA Negeri 1 Gianyar</strong> mengumpulkan, menggunakan,
        dan melindungi data pengguna (siswa, guru, dan staf administrasi sekolah).
    </p>

    <h2>1. Data yang Kami Kumpulkan</h2>
    <table>
        <tr><th>Jenis Data</th><th>Keterangan</th></tr>
        <tr><td>Data identitas</td><td>Nama, NIS/NISN (siswa), NIP (guru/staf), email, kelas/jabatan</td></tr>
        <tr><td>Lokasi (GPS)</td><td>Diambil hanya saat proses presensi, untuk memvalidasi bahwa pengguna berada di area sekolah (geofencing) dan mendeteksi lokasi palsu (fake GPS/mock location)</td></tr>
        <tr><td>Foto</td><td>Foto selfie saat presensi, dan foto bukti untuk prestasi/pelanggaran/aset sekolah yang diunggah pengguna</td></tr>
        <tr><td>ID perangkat</td><td>Digunakan untuk mencegah "titip absen" (satu akun mencoba presensi dari banyak perangkat berbeda)</td></tr>
        <tr><td>Data akademik</td><td>Nilai, jadwal, presensi, catatan perilaku (BK), prestasi, dan data terkait lain yang dikelola sekolah</td></tr>
    </table>

    <h2>2. Bagaimana Data Digunakan</h2>
    <ul>
        <li>Mencatat dan memvalidasi kehadiran siswa/guru secara otomatis</li>
        <li>Mencegah kecurangan presensi (lokasi palsu, titip absen)</li>
        <li>Menyediakan informasi akademik (nilai, jadwal, prestasi) kepada siswa dan wali kelas</li>
        <li>Keperluan administrasi sekolah lainnya (BK, sarana-prasarana, dsb.)</li>
    </ul>
    <p>Kami <strong>tidak</strong> menggunakan data untuk iklan, dan <strong>tidak</strong> menjual atau membagikan data ke pihak ketiga di luar keperluan operasional sekolah.</p>

    <h2>3. Penyimpanan &amp; Keamanan</h2>
    <p>
        Seluruh komunikasi antara aplikasi dan server dienkripsi menggunakan HTTPS. Data disimpan di server
        yang dikelola oleh pihak sekolah dan hanya dapat diakses oleh pihak yang berwenang (admin, guru wali,
        BK) sesuai kebutuhan tugasnya masing-masing.
    </p>

    <h2>4. Data Anak / Siswa di Bawah Umur</h2>
    <p>
        Aplikasi ini digunakan dalam konteks pendidikan resmi oleh SMA Negeri 1 Gianyar. Pendaftaran akun
        siswa dilakukan oleh pihak sekolah sebagai bagian dari proses administrasi akademik, bukan pendaftaran
        mandiri oleh anak.
    </p>

    <h2>5. Hak Pengguna</h2>
    <p>
        Pengguna dapat menghubungi pihak sekolah untuk meminta akses, koreksi, atau penghapusan data pribadi
        sesuai peraturan perundang-undangan yang berlaku (termasuk UU No. 27 Tahun 2022 tentang Pelindungan
        Data Pribadi).
    </p>

